public class AddValues {

    /*
    Welcome to this video. Now we will make some methods to see how they are working
    In this class we will define two methods that adds numbers and return the sum (the result)
    And then in a new class we will use this functionality. Let's go!
    */
    
    public int addTwoValues(int a, int b) {
        // Here we create a method that takes two integers, adds them and returns the sum
        return a+b;
    }
    
    //Let's do the same for three integers
    public int addThreeValues(int a, int b, int c) {
        return a+b+c;
    }
    
}
